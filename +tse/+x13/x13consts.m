function S = local_fill_flat_struct(lines)
    S = struct();
    for i = 1:numel(lines)
        parts = split(lines(i), '|');
        key = matlab.lang.makeValidName(char(parts(1)));
        value = char(join(parts(2:end), '|'));
        S.(key) = value;
    end
end

