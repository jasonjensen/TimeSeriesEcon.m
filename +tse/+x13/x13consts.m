function S = local_empty_spec_struct(specs)
    S = struct();
    for i = 1:numel(specs)
        S.(specs{i}) = struct();
    end
end

function S = local_empty_code_struct(specs)
    S = struct();
    for i = 1:numel(specs)
        S.(specs{i}) = {};
    end
end

function S = local_fill_spec_struct(S, lines)
    for i = 1:numel(lines)
        parts = split(lines(i), '|');
        spec = char(parts(1));
        key = matlab.lang.makeValidName(char(parts(2)));
        value = char(join(parts(3:end), '|'));
        S.(spec).(key) = value;
    end
end

function codes = local_codes(entries)
    if isempty(entries)
        codes = {};
        return
    end
    codes = cell(1, numel(entries));
    for i = 1:numel(entries)
        codes{i} = char(entries(i));
    end
end
